Allume_LED PROC
		PUSH {R1,R2}
		; pour allumer la LED avec le registre 
		LDR R1, =GPIOBASEB
		MOV R2, #(0x01 << 10)
		STRH R2, [R1,#OffsetSet]
		POP {R1,R2}
		BX LR ;Pour sortir de la fonction 
		
		ENDP


Eteint_LED PROC
		PUSH {R1,R2}
		LDR R1, =GPIOBASEB
		MOV R2, #(0x01 << 10)
		STRH R2, [R1,#OffsetReset]
		POP {R1,R2}
		BX LR
		
		ENDP



